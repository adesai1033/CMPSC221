    private void studentScheduleCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentScheduleCourseButtonActionPerformed
        String courseCode = studentScheduleCourseComboBox1.getSelectedItem().toString();
        
        String fullName = studentScheduleCourseComboBox2.getSelectedItem().toString();
        
        
        String[] names = fullName.split(" ");
        String studentID = names[2];
        
        java.sql.Timestamp timestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        
        int seatsTotal = CourseQueries.getCourseSeats(currentSemester,courseCode);
        int seatsTaken = ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);
        
        ArrayList<ScheduleEntry> scheduledStudents = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, courseCode);
        for (ScheduleEntry entry:scheduledStudents) 
        {
            
            if (entry.getStudentID().equals(studentID)) 
            {
                studentScheduleCourseStatusLabel.setText(fullName +" has already scheduled this course");
                return;
            }
        }
        if (seatsTotal>seatsTaken)
        {
            ScheduleEntry student = new ScheduleEntry(currentSemester, studentID, courseCode, "s", timestamp);//
            ScheduleQueries.addScheduleEntry(student);
            studentScheduleCourseStatusLabel.setText(studentScheduleCourseComboBox1.getSelectedItem().toString() +" has been scheduled for the current semester.");
            
        }
        else{
            ScheduleEntry student = new ScheduleEntry(currentSemester, studentID, courseCode, "w", timestamp);
            ScheduleQueries.addScheduleEntry(student);
            studentScheduleCourseStatusLabel.setText(studentScheduleCourseComboBox1.getSelectedItem().toString() +" has been waitlisted for the current semester.");
        }
    }//GEN-LAST:event_studentScheduleCourseButtonActionPerformed

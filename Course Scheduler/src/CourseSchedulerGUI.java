    public void rebuildSemesterComboBoxes()
    {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        changeSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0)
        {
            currentSemesterLabel2.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        }
        else
        {
            currentSemesterLabel2.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    
    public void rebuildCourseComboBoxes()
    {
        ArrayList<String> courses = CourseQueries.getAllCourseCodes(currentSemester);
        studentScheduleCourseComboBox1.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        studentDropCourseComboBox1.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        adminCourseListComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        adminDropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        
    }
    //should display userID
    public void rebuildStudentComboBoxes()
    {
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        ArrayList<String> studentNames = new ArrayList<>();
        
        for (StudentEntry student:students)
        {
            String name = student.getFirstName() + " " + student.getLastName() + " " + student.getStudentID();
             
            studentNames.add(name);
        }
        studentScheduleCourseComboBox2.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        studentDisplayCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        adminDropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        studentDropCourseComboBox2.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        
    }
    
    

